#!/usr/bin/env node

const fs = require('node:fs/promises');
const path = require('node:path');

const gate = process.argv[2] || 'unknown';
const repoRoot = path.resolve(__dirname, '..', '..');
const defaultPlansDir = path.resolve(repoRoot, '..', '..', 'plans');
const plansDir = process.env.JELLYRIN_PLANS_DIR || defaultPlansDir;
const generatedDir = path.join(plansDir, 'generated');

async function main() {
  await fs.mkdir(generatedDir, { recursive: true });
  const evidencePath = path.join(generatedDir, `${gate}.json`);
  const evidence = {
    gate,
    status: 'not-started',
    percent: 0,
    closed: false,
    sourcePhase: '0027',
    evidence: 'placeholder generated by E0; no new ecosystem implementation evidence yet',
    updatedAt: new Date().toISOString(),
    openRisks: ['Placeholder only; replace with a real golden trace for this evolutive.'],
  };
  await fs.writeFile(evidencePath, `${JSON.stringify(evidence, null, 2)}\n`);
  console.log(`wrote ${evidencePath}`);
  console.log(`Ecosystem gate "${gate}" is not started yet. Run npm run evidence:ecosystem to inspect roadmap status.`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
